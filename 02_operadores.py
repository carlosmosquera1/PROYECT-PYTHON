### OPERADORES ###


print(1 + 2)  # Suma
print(5 - 3)  # Resta
print(4 * 2)  # Multiplicación
print(8 / 2)  # División
print(5 % 2)  # Módulo (resto de la división)
print(2 ** 3)  # Exponente (2 elevado a la 3)
print()


### OPERADORES COMPARATIVOS ###

print(5 > 3)  # Mayor que
print(5 < 3)  # Menor que
print(5 >= 5)  # Mayor o igual que
print(5 <= 5)  # Menor o igual que
print(5 == 5)  # Igualdad
print(5 != 3)  # Diferente de
print()

print('hola' > 'adios')  # Comparación de cadenas (lexicográfica)
print('hola' < 'adios')  # Comparación de cadenas (lexicográfica
print('hola' == 'hola')  # Igualdad de cadenas
print('hola' != 'adios')  # Diferente de cadenas
print()

